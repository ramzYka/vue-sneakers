<script setup>

    import { inject } from 'vue';
    import CartItem from './CartItem.vue';

    const {cart, removeFromCart} = inject('cart')

</script>

<template>

<div v-auto-animate class="flex flex-col gap-5">
    <CartItem
    v-for="item in cart" 
    :key="item.id" 
    :title="item.title" 
    :price="item.price" 
    :imageUrl="item.imageUrl"
    @on-click-remove="() => removeFromCart(item)"
    >
    </CartItem>


</div>

</template>